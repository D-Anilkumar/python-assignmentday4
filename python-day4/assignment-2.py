

# Python code for implementation of isupper() 
  
# checking for uppercase characters 
string = 'LETSUPGRADE'
print(string.isupper()) 
  
string = 'letsupgrade'
print(string.isupper()) 


# Python code for implementation of isupper() 
  
# checking for lowercase characters 
string = 'letsupgrade'
print(string.islower()) 
  
string = 'letsupgrade'
print(string.islower()) 


